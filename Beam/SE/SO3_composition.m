function q_3 = SO3_composition(q_1, q_2)

n_cols = size(q_1, 2);

q_3 = zeros(4, n_cols);
q_3(1, :) = q_1(1, :) * q_2(1) - q_1(2, :) * q_2(2) -  q_1(3, :) * q_2(3) -  q_1(4, :) * q_2(4);
q_3(2, :) = q_1(1, :) * q_2(2) + q_1(2, :) * q_2(1) +  q_1(3, :) * q_2(4) -  q_1(4, :) * q_2(3);
q_3(3, :) = q_1(1, :) * q_2(3) + q_1(3, :) * q_2(1) +  q_1(4, :) * q_2(2) -  q_1(2, :) * q_2(4);
q_3(4, :) = q_1(1, :) * q_2(4) + q_1(4, :) * q_2(1) +  q_1(2, :) * q_2(3) -  q_1(3, :) * q_2(2);

end
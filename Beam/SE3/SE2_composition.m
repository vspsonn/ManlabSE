function f_3 = SE2_composition(f_1, f_2)

f_3 = zeros(4,1);
f_3(1) = f_1(1) * f_2(1) - f_1(2) * f_2(2);
f_3(2) = f_1(1) * f_2(2) + f_1(2) * f_2(1);
sca_0 = (1. - 2. * f_1(2)^2);
sca_1 = 2. * f_1(1) * f_1(2);
f_3(3) = f_1(3) + sca_0 * f_2(3) - sca_1 * f_2(4);
f_3(4) = f_1(4) + sca_0 * f_2(4) + sca_1 * f_2(3);

end